"""ansi_colors.py - Shared ANSI color definitions"""

ERROR_COLOR = "\033[91m"       # Red for errors
SUCCESS_COLOR = "\033[32m"     # Green for success messages
WARNING_COLOR = "\033[93m"     # Yellow for warnings (e.g. time outs)
INFO_COLOR = "\033[95m"        # Purple for informational messages
HIGHLIGHT_COLOR = "\033[96m"   # Cyan for important highlights
RESET_COLOR = "\033[0m"        # Reset for normal text
